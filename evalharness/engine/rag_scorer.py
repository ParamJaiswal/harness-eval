"""RAG-specific scoring metrics inspired by RAGAS.

All metrics are computed purely with keyword overlap and TF-IDF-like
heuristics — no ML models required — making the scorer self-contained and
fast.
"""

from __future__ import annotations

import math
import re
from collections import Counter
from dataclasses import dataclass
from typing import Sequence

from evalharness.adapters.base import RetrievedContext

# ---------------------------------------------------------------------------
# Stop words for keyword extraction
# ---------------------------------------------------------------------------

_STOP_WORDS = frozenset({
    "the", "a", "an", "is", "are", "was", "were", "be", "been", "being",
    "have", "has", "had", "do", "does", "did", "will", "would", "could",
    "should", "may", "might", "can", "shall", "to", "of", "in", "for",
    "on", "with", "at", "by", "from", "as", "into", "through", "during",
    "before", "after", "what", "which", "who", "whom", "this", "that",
    "these", "those", "it", "and", "or", "but", "not", "no", "if", "than",
    "how", "when", "where", "why", "i", "you", "he", "she", "we", "they",
    "me", "him", "her", "us", "them", "my", "your", "his", "its", "our",
    "their",
})


@dataclass
class RAGScorecard:
    """Multi-dimensional RAG quality scorecard."""

    faithfulness: float
    context_relevance: float
    answer_relevance: float
    retrieval_precision: float
    retrieval_recall: float
    overall_score: float


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _tokenize(text: str) -> list[str]:
    """Lowercase tokenisation, removing stop words."""
    return [w for w in re.findall(r"\w+", text.lower()) if w not in _STOP_WORDS]


def _token_set(text: str) -> set[str]:
    return set(_tokenize(text))


def _tf_vector(tokens: list[str]) -> Counter:
    return Counter(tokens)


def _cosine_similarity(a: Counter, b: Counter) -> float:
    """Compute cosine similarity between two term-frequency counters."""
    common = set(a) & set(b)
    if not common:
        return 0.0
    dot = sum(a[k] * b[k] for k in common)
    norm_a = math.sqrt(sum(v * v for v in a.values()))
    norm_b = math.sqrt(sum(v * v for v in b.values()))
    if norm_a == 0 or norm_b == 0:
        return 0.0
    return dot / (norm_a * norm_b)


# ---------------------------------------------------------------------------
# Public scoring functions
# ---------------------------------------------------------------------------


def score_rag_response(
    question: str,
    answer: str,
    retrieved_contexts: list[RetrievedContext],
    ground_truth_contexts: list[str],
    ground_truth_answer: str,
) -> RAGScorecard:
    """Compute all RAG quality metrics and return a scorecard.

    Parameters
    ----------
    question:
        The user's original question.
    answer:
        The answer generated by the RAG pipeline.
    retrieved_contexts:
        The contexts returned by the retriever.
    ground_truth_contexts:
        The ideal contexts that should have been retrieved.
    ground_truth_answer:
        The expected (gold) answer.
    """
    context_texts = [c.text for c in retrieved_contexts]

    faith = compute_faithfulness(answer, context_texts)
    ctx_rel = compute_context_relevance(question, context_texts)
    ans_rel = compute_answer_relevance(question, answer)
    precision, recall = compute_retrieval_precision_recall(
        context_texts, ground_truth_contexts
    )

    overall = (
        0.25 * faith
        + 0.20 * ctx_rel
        + 0.25 * ans_rel
        + 0.15 * precision
        + 0.15 * recall
    )

    return RAGScorecard(
        faithfulness=round(faith, 4),
        context_relevance=round(ctx_rel, 4),
        answer_relevance=round(ans_rel, 4),
        retrieval_precision=round(precision, 4),
        retrieval_recall=round(recall, 4),
        overall_score=round(overall, 4),
    )


def compute_faithfulness(answer: str, contexts: list[str]) -> float:
    """Check if the answer is grounded in the retrieved contexts.

    Uses keyword overlap: what fraction of answer keywords appear in at least
    one context?  A perfectly faithful answer would use only words from the
    context.
    """
    if not answer.strip() or not contexts:
        return 0.0

    answer_tokens = _token_set(answer)
    if not answer_tokens:
        return 0.0

    context_tokens: set[str] = set()
    for ctx in contexts:
        context_tokens |= _token_set(ctx)

    grounded = answer_tokens & context_tokens
    return len(grounded) / len(answer_tokens)


def compute_context_relevance(question: str, contexts: list[str]) -> float:
    """Check if retrieved contexts are relevant to the question.

    Computes average cosine similarity between the question and each context
    using TF vectors.
    """
    if not contexts or not question.strip():
        return 0.0

    q_tf = _tf_vector(_tokenize(question))
    scores: list[float] = []
    for ctx in contexts:
        c_tf = _tf_vector(_tokenize(ctx))
        scores.append(_cosine_similarity(q_tf, c_tf))

    return sum(scores) / len(scores) if scores else 0.0


def compute_answer_relevance(question: str, answer: str) -> float:
    """Check if the answer addresses the question.

    Uses cosine similarity between question and answer TF vectors, plus a
    bonus for length-appropriateness.
    """
    if not question.strip() or not answer.strip():
        return 0.0

    q_tf = _tf_vector(_tokenize(question))
    a_tf = _tf_vector(_tokenize(answer))
    sim = _cosine_similarity(q_tf, a_tf)

    # Length bonus — answers that are at least 20 words get a small boost
    word_count = len(answer.split())
    length_bonus = min(0.15, word_count / 200)

    return min(1.0, sim + length_bonus)


def compute_retrieval_precision_recall(
    retrieved_contexts: list[str],
    ground_truth_contexts: list[str],
) -> tuple[float, float]:
    """Compute precision@K and recall@K using fuzzy matching.

    A retrieved context is considered a match if its cosine similarity with
    any ground-truth context exceeds 0.3 (generous threshold for keyword
    overlap).
    """
    if not retrieved_contexts:
        return 0.0, 0.0
    if not ground_truth_contexts:
        # No ground truth — every retrieval is "correct" by vacuous truth
        return 1.0, 1.0

    match_threshold = 0.3
    matched_gt: set[int] = set()
    relevant_retrieved = 0

    for ret in retrieved_contexts:
        ret_tf = _tf_vector(_tokenize(ret))
        for gi, gt in enumerate(ground_truth_contexts):
            gt_tf = _tf_vector(_tokenize(gt))
            if _cosine_similarity(ret_tf, gt_tf) >= match_threshold:
                relevant_retrieved += 1
                matched_gt.add(gi)
                break  # one match per retrieved context is enough

    precision = relevant_retrieved / len(retrieved_contexts)
    recall = len(matched_gt) / len(ground_truth_contexts)
    return precision, recall
